package com.atsapp.model;

public class Company {
	
	
	private int cm_id;
	
	private String cm_name;

	private String cm_desc;
	
	private String cm_contact;

	public int getCm_id() {
		return cm_id;
	}

	public void setCm_id(int cm_id) {
		this.cm_id = cm_id;
	}

	public String getCm_name() {
		return cm_name;
	}

	public void setCm_name(String cm_name) {
		this.cm_name = cm_name;
	}

	public String getCm_desc() {
		return cm_desc;
	}

	public void setCm_desc(String cm_desc) {
		this.cm_desc = cm_desc;
	}

	public String getCm_contact() {
		return cm_contact;
	}

	public void setCm_contact(String cm_contact) {
		this.cm_contact = cm_contact;
	}
	
	
	
}
